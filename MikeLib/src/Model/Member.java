package Model;

/**
 *
 * @author mac
 */
public class Member {
    private int id;
    private String idNumber;
    private String name;
    private String phoneNumber;
    private String email;

    public Member() {
    }

    public Member(int id, String idNumber, String name, String phoneNumber, String email) {
        this.id = id;
        this.idNumber = idNumber;
        this.name = name;
        this.phoneNumber = phoneNumber;
        this.email = email;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getIdNumber() {
        return idNumber;
    }

    public void setIdNumber(String idNumber) {
        this.idNumber = idNumber;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public String toString() {
        return "Member{" + "id=" + id + ", idNumber=" + idNumber + ", name=" + name + ", phoneNumber=" + phoneNumber + ", email=" + email + '}';
    }
    
    
}
